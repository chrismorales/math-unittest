
"""Simple math functions"""
"""These functions should only be used with number types"""

"""Takes in two arguments and returns the addition of the two"""
def add(x, y):
    return x + y

"""Takes in two arguments and returns the multipication of the two"""
def multiply(x, y):
    return x * y

"""Takes in two arguments and returns the division of the first argument by the second argument"""
"""For example, divide(6,3) will calculate '6 / 3' and return 2"""
def divide(x, y):
    return x / y

"""Takes in two arguments and returns the subtraction of the second argument from the first argument"""
"""For example, subtract(1,2) will calculate '1 - 2' and return -1"""
def subtract(x, y):
    return x - y

